naive_bayes_model = GaussianNB()
naive_bayes_model.fit(X_train, y_train)

print("Gaussian Naive Bayes model trained successfully! (Note: GaussianNB is typically used for classification. Further steps might be needed depending on the exact use case for 'Salary' prediction.)")

"""### Support Vector Regressor (SVR) Model"""

from sklearn.svm import SVR

# Initialize and train the Support Vector Regressor model
# SVR can be computationally intensive on large datasets, and parameter tuning is crucial.
# For demonstration, we'll use default parameters.
svr_model = SVR()
svr_model.fit(X_train, y_train)

print("Support Vector Regressor model trained successfully!")

"""### Model Performance Comparison: Visual Assessment"""

import matplotlib.pyplot as plt
import seaborn as sns

# Make predictions on the test set for each model
y_pred_linear = linear_reg_model.predict(X_test)
y_pred_knn = knn_model.predict(X_test)
y_pred_decision_tree = decision_tree_model.predict(X_test)
y_pred_random_forest = random_forest_model.predict(X_test)
# Naive Bayes (GaussianNB) for regression is atypical, but we'll include its predictions for completeness
y_pred_naive_bayes = naive_bayes_model.predict(X_test)
y_pred_svr = svr_model.predict(X_test)

# Create a figure with subplots for each model
fig, axes = plt.subplots(nrows=3, ncols=2, figsize=(15, 18))
axes = axes.flatten()

models = {
    "Linear Regression": y_pred_linear,
    "K-Nearest Neighbors": y_pred_knn,
    "Decision Tree": y_pred_decision_tree,
    "Random Forest": y_pred_random_forest,
    "Naive Bayes": y_pred_naive_bayes,
    "Support Vector Regressor": y_pred_svr
}

for i, (model_name, y_pred) in enumerate(models.items()):
    sns.scatterplot(x=y_test, y=y_pred, ax=axes[i], alpha=0.6)
    axes[i].plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], 'r--', lw=2) # Ideal prediction line
    axes[i].set_title(f'{model_name}: Actual vs. Predicted Salary')
    axes[i].set_xlabel('Actual Salary')
    axes[i].set_ylabel('Predicted Salary')

plt.tight_layout()
plt.show()

"""### Quantitative Model Comparison with Bar Graphs"""

from sklearn.metrics import r2_score, mean_absolute_error
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Dictionary to store performance metrics
performance_metrics = {
    'Model': [],
    'R-squared': [],
    'MAE': []
}

# Calculate metrics for each model
for model_name, y_pred in models.items():
    # Note: GaussianNB output might need careful handling if it produces non-numeric or unexpected values for a regression task
    # For this demonstration, we'll try to calculate, but be aware of the classification nature of GaussianNB.
    try:
        r2 = r2_score(y_test, y_pred)
        mae = mean_absolute_error(y_test, y_pred)
    except Exception as e:
        print(f"Could not calculate metrics for {model_name}: {e}")
        r2 = None
        mae = None

    performance_metrics['Model'].append(model_name)
    performance_metrics['R-squared'].append(r2)
    performance_metrics['MAE'].append(mae)

# Create a DataFrame from the metrics
metrics_df = pd.DataFrame(performance_metrics)

# Display the metrics table
print("\nModel Performance Metrics:")
display(metrics_df)

# Create bar graphs
fig, axes = plt.subplots(nrows=1, ncols=2, figsize=(16, 6))

# R-squared bar graph
sns.barplot(x='Model', y='R-squared', data=metrics_df.dropna(subset=['R-squared']).sort_values(by='R-squared', ascending=False), ax=axes[0])
axes[0].set_title('Model R-squared Comparison')
axes[0].set_ylabel('R-squared Score')
axes[0].set_xlabel('Model')
axes[0].tick_params(axis='x', rotation=45)

# MAE bar graph
sns.barplot(x='Model', y='MAE', data=metrics_df.dropna(subset=['MAE']).sort_values(by='MAE', ascending=True), ax=axes[1])
axes[1].set_title('Model Mean Absolute Error (MAE) Comparison')
axes[1].set_ylabel('Mean Absolute Error')
axes[1].set_xlabel('Model')
axes[1].tick_params(axis='x', rotation=45)

plt.tight_layout()
plt.show()

